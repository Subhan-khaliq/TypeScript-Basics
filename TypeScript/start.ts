//explicit
let firstName: string = "Dylan";
let age: number = 36;

function myFunction()
{
  console.log("My first name is",firstName,"and my age is",age);

}

myFunction();


//implicit

let lastName = "David";
let height   = 5.6;

function myInformation()
{
  console.log("My last name is of type",typeof lastName,"and my height is of type",typeof height);
}

myInformation();
